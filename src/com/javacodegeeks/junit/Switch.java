package com.javacodegeeks.junit;

public class Switch {

	public static void main(String[] args) {
		//Float f = new Float("12"); 
		int f = 2;
		switch (f) 
		{
		    case 12: System.out.println("Twelve"); 
		    case 0: System.out.println("Zero"); 
		    default: System.out.println("Default"); 
		}

	}

}
